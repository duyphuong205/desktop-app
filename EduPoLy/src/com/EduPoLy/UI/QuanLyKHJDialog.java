    void reset() {
        KhoaHoc kh = new KhoaHoc();
        ChuyenDe chuyende = (ChuyenDe) cbochuyende.getSelectedItem();//lay chuyen de dc chon o combobox
        kh.setMaNV(AUTH.user.getMaNV());
        kh.setMaCD(chuyende.getMaCD());
        kh.setNgayTao(XDATE.now());
        this.setform(kh);
        this.row = -1;
        this.updatestatus();
    }

    void edit() {
        Integer makh = (Integer) tblqlkhoahoc.getValueAt(this.row, 0);
        KhoaHoc kh = khdao.selectByID(makh);
        this.setform(kh);
        tabs.setSelectedIndex(0);
        this.updatestatus();
    }

    void setform(KhoaHoc kh) {
        //luu makh vao tam settooltiptext vi khong co cho tren form  
        //makh la identity
//        cbochuyende.setToolTipText(String.valueOf(kh.getMaKH()));
        cbochuyende.setSelectedItem(cddao.selectByID(kh.getMaCD()));
        txtngaykg.setText(XDATE.toString(kh.getNgayKG(), "dd/MM/yyyy"));
        txthocphi.setText(String.valueOf(kh.getHocPhi()));
        txtthoiluong.setText(String.valueOf(kh.getThoiLuong()));
        txtnguoitao.setText(kh.getMaNV());
        txtngaytao.setText(XDATE.toString(kh.getNgayTao(), "dd/MM/yyyy"));
        txtghichu.setText(kh.getGhiChu());
    }

    KhoaHoc getform() {
        KhoaHoc kh = new KhoaHoc();
        ChuyenDe chuyenDe = (ChuyenDe) cbochuyende.getSelectedItem();
//        kh.setMaKH(Integer.valueOf(cbochuyende.getToolTipText())); 
        kh.setMaCD(chuyenDe.getMaCD());
        kh.setHocPhi(Double.valueOf(txthocphi.getText()));
        kh.setThoiLuong(Integer.valueOf(txtthoiluong.getText()));
        kh.setNgayKG(XDATE.toDate(txtngaykg.getText(), "dd/MM/yyyy"));
        kh.setGhiChu(txtghichu.getText());
        kh.setMaNV(AUTH.user.getMaNV());
        kh.setNgayTao(XDATE.now());

        return kh;
    }

    void fisrt() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblqlkhoahoc.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblqlkhoahoc.getRowCount() - 1;
        this.edit();
    }

    void updatestatus() {
        boolean edit = (this.row >= 0);
        boolean fisrt = (this.row == 0);
        boolean last = (this.row == tblqlkhoahoc.getRowCount() - 1);
        //trang thai form
        lbltencd.setEnabled(!edit);//khong duoc cap nhap ma.Khi nao gia tri edit=false thi moi dc cap nhap va nguoc lai

        btnthem.setEnabled(!edit);//khi hien thong tin tren o nhap thi khong hien button va nguoc lai
        btnxoa.setEnabled(edit);//khi co thong tin (true) thi hien buuton va nguoc lai
        btnsua.setEnabled(edit);//nhu tren
        //cac thanh dieu huong
        //khi trong trang thai edit thi moi hien button va nguoc lai
        btnfisrt.setEnabled(edit && !fisrt);//chi hien len khi dang edit va khong phai la hang dau tien
        btnprevious.setEnabled(edit && !fisrt);//nhu tren
        btnnext.setEnabled(edit && !last);//chi hien len khi dang edit va khong phai la hang cuoi cung
        btnlast.setEnabled(edit && !last);//nhu tren
    }


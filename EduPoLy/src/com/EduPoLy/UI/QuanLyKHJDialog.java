    private void init() {
        this.setLocationRelativeTo(null);
        setTitle("QUẢN LÝ KHÓA HỌC");
        this.fillcomboboxchuyende();

    }
    KhoaHocDAO khdao = new KhoaHocDAO();
    ChuyenDeDAO cddao = new ChuyenDeDAO();
    int row = -1;
    DefaultTableModel model;

    //Khi chọn 1 chuyên đề trên CBB thì đỗ dữ liêu vào bảng 
    void filltable() {
        model = (DefaultTableModel) tblqlkhoahoc.getModel();
        model.setRowCount(0);
        try {
            //lấy chuyên đề đc chọn
            ChuyenDe chuyende = (ChuyenDe) cbochuyende.getSelectedItem();
            //lấy ra danh sách khoahoc phụ thuộc vào chuyên đề đc chọn
            List<KhoaHoc> list = khdao.selectbyChuyende(chuyende.getMaCD());
            for (KhoaHoc kh : list) {
                model.addRow(new Object[]{kh.getMaKH(), kh.getHocPhi(), kh.getThoiLuong(), XDATE.toString(kh.getNgayKG(), "dd/MM/yyyy"),
                    kh.getMaNV(), XDATE.toString(kh.getNgayTao(), "dd/MM/yyyy")});
            }
        } catch (Exception e) {
            MsgBOX.alert(this, "Loi truy van du lieu!!!");
        }
    }

    //Lấy đối tượng model CBB ,quét sạch dữ liệu sau đó đổ tất cả các chuyển đề lên CBB
    //Hàm này phải đc gọi vào hàm init;
    void fillcomboboxchuyende() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbochuyende.getModel();
        model.removeAllElements();
        List<ChuyenDe> list = cddao.selectAll();
        for (ChuyenDe cd : list) {
            model.addElement(cd);
        }
    }

    void chonchuyende() {
        //lấy chuyên đề được chọn trong CBB
        ChuyenDe chuyende = (ChuyenDe) cbochuyende.getSelectedItem();
        //Hiển thị 1 số dữ liệu mặc định của khóa học lên form
        txtthoiluong.setText(String.valueOf(chuyende.getThoiLuong()));
        txthocphi.setText(String.valueOf(chuyende.getHocPhi()));
        lbltencd.setText(chuyende.getTenCD());
        txtghichu.setText(chuyende.getTenCD());

        //Đỗ lại dữ liệu khóa học lên bảng khi chuyên đề thay đổi
        this.filltable();
        this.row = -1;
        this.updatestatus();
        tabs.setSelectedIndex(1);
    }

    boolean isValidate() {
        if (txtngaykg.getText().length() == 0) {
            MsgBOX.alert(this, "BAN CHUA NHAP NGAY!!!");
            txtngaykg.requestFocus();
            return false;
        }
        try {
            XDATE.toDate(txtngaykg.getText(), "dd/MM/yyyy");
        } catch (Exception e) {
            MsgBOX.alert(this, "NGAY KHONG DUNG DINH DANG(dd/MM/yyyy)");
            txtngaykg.requestFocus();
            txtngaykg.setText("");
            return false;
        }
        if (txthocphi.getText().length() == 0) {
            MsgBOX.alert(this, "BAN CHUA NHAP HOC PHI!!!");
            txthocphi.requestFocus();
            return false;
        }
        try {
            Double HP = Double.parseDouble(txthocphi.getText());
            if (HP <= 0) {
                throw new Exception();
            }
        } catch (Exception e) {
            MsgBOX.alert(this, "HOC PHI PHAI LA SO THUC VA PHAI >0!!!");
            txthocphi.setText("");
            txthocphi.requestFocus();
            return false;
        }
        if (txtthoiluong.getText().length() == 0) {
            MsgBOX.alert(this, "BAN CHUA NHAP THOI LUONG!!!");
            txtthoiluong.requestFocus();
            return false;
        }
        try {
            Integer TL = Integer.parseInt(txtthoiluong.getText());
            if (TL <= 0) {
                throw new Exception();
            }
        } catch (Exception e) {
            MsgBOX.alert(this, "THOI LUONG PHAI LA SO NGUYEN VA PHAI >0!!!");
            txtthoiluong.setText("");
            txtthoiluong.requestFocus();
            return false;
        }
        if (txtghichu.getText().length() == 0) {
            MsgBOX.alert(this, "NHAP GHI CHU!!!");
            txtghichu.requestFocus();
            return false;
        }
        return true;
    }

